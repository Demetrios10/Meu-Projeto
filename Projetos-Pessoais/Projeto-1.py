# Comparando se Numero 1 é maior que Numero 2 e se os mesmos São iguais

numero_1 = int (input("Digite um Numero: "))
numero_2 = int (input("Digite outro numero: "))

maior = numero_1 > numero_2
print("O Numero 1 é maior do que o Numero 2 ? ", maior , "\n")

resultado = numero_1 == numero_2
print("Os Numeros são Iguais!!", resultado , "\n")











