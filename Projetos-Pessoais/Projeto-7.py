# Contagem de Letras

lista = ('D','E','M','É','T','R','I','O','S')
for letra_falta in lista:
     if(letra_falta == 'D'):
          print(f'{letra_falta} = Letra 1')
     elif(letra_falta == 'E'):
          print(f'{letra_falta} = Letra 2')
     elif(letra_falta == 'M'):
          print(f'{letra_falta} = Letra 3')
     elif(letra_falta == 'É'):
          print(f'{letra_falta} = Letra 4')
     elif(letra_falta == 'T'):
          print(f'{letra_falta} = Letra 5')
     elif(letra_falta == 'R'):
          print(f'{letra_falta} = Letra 6')
     elif(letra_falta == 'I'):
          print(f'{letra_falta} = Letra 7')
     elif(letra_falta == 'O'):
          print(f'{letra_falta} = Letra 8')
     elif(letra_falta == 'S'):
          print(f'{letra_falta} = Letra 9')
   
