# Imputado Dados no Python

x = int (input('Digite um número: '))
y = int (input('Digite Outro Numero: '))

soma = x + y
subtracao = x - y
divisao = x / y
modulo = x % y
multiplicacao = x * y
elevacao = x ** y

print("\nA Soma dos Valores é: ",soma)
print("A Subtração dos Valores é: ",subtracao)
print("A Divisão entre os Valores é: ",divisao)
print("O modulo entre os  Valores é: ",modulo)
print("O multiplicação entre os  Valores é: ",multiplicacao)
print("A elevação dos Valores é: ",elevacao)