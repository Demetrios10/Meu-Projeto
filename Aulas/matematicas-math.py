# antes importe o modulo math
import math

x = 8 
y = 100

raiz_quadrada = math.sqrt(x) # raiz quadrada 
print(raiz_quadrada)
print(math.ceil(raiz_quadrada)) # arredonda o valor para cima 
print(math.floor(raiz_quadrada)) # arredonda o valor para baixo 
print(round(raiz_quadrada,2)) # valor com duas casas 


logaritmo = math.log10(y)
print(logaritmo)

print(math.pi) # valor do PI 
print(math.factorial(x))
print(x / math.inf)